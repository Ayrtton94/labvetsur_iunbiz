     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validator = Validator::make(['nombre' => $request->get('nombre')], [
            'nombre' => ' required|string|max:100|unique:categorias',
        ]);

        if ($validator->fails()) {
            return redirect()->back()->with('error', 'la categoria ya existe');
        }

        $categoria_data = request()->except('_token');
        Categorias::create($categoria_data);
        return redirect('categoria')->with('info','La categoria se creó con éxito');
    }

